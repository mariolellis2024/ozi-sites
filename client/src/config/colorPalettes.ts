/** Helper: build a CSS string that overrides :root variables for a given palette */
export function buildPaletteCSS(palette: ColorPalette): string {
  return Object.entries(palette.variables)
    .map(([key, value]) => `${key}: ${value};`)
    .join('\n  ');
}
